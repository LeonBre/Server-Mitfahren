package apiv1.models;

public class OtherDrive {
	String driveId;
	String destination;
	String arrival;
	String date;
	
	public OtherDrive(String driveId, String destination, String arrival, String date) {
		this.driveId = driveId;
		this.destination = destination;
		this.arrival = arrival;
		this.date = date;
	}
	
	
}